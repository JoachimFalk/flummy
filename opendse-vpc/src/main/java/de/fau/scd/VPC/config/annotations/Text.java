package de.fau.scd.VPC.config.annotations;

import static java.lang.annotation.ElementType.FIELD;
import static java.lang.annotation.ElementType.METHOD;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/**
 * The {@link Text} annotation marks a {@link String} property as a multi-line text field.
 * 
 * @author Joachim Falk
 */
@Retention(RUNTIME)
@Target({ METHOD, FIELD })
public @interface Text {
    
    /**
     * The annotated property is String that will be inputed via a JTextArea.
     * This method returns how many rows this text area should show. Defaults to 5.
     * 
     * @return How many rows the text area should show.
     */
    int rows() default 5;    
}